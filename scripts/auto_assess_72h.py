#!/usr/bin/env python3
"""
MOSS 72h Experiment Auto-Assessment
====================================

72小时实验完成后的自动评估脚本

当72h实验完成后，运行此脚本自动生成评估报告

Usage:
    python3 scripts/auto_assess_72h.py --data experiments/real_world_actions_resumed.jsonl
"""

import json
import argparse
import subprocess
from pathlib import Path
from datetime import datetime


def check_experiment_status():
    """检查72h实验状态"""
    status_file = Path("experiments/primary_72h_status.json")
    
    if not status_file.exists():
        print("❌ 找不到实验状态文件")
        return None
    
    with open(status_file) as f:
        status = json.load(f)
    
    return status


def run_analysis(data_file: str, output_dir: str):
    """运行分析脚本"""
    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)
    
    print("\n🔍 Running analysis tools...")
    
    # 1. 综合结果分析
    print("  1. Analyzing 72h results...")
    result1 = subprocess.run([
        "python3", "scripts/analyze_72h_results.py",
        "--input", data_file,
        "--output", str(output_path / "analysis")
    ], capture_output=True, text=True)
    
    if result1.returncode == 0:
        print("     ✅ Complete")
    else:
        print(f"     ⚠️  Warning: {result1.stderr[:100]}")
    
    # 2. Purpose演化可视化
    print("  2. Visualizing purpose evolution...")
    result2 = subprocess.run([
        "python3", "scripts/visualize_purpose_evolution.py",
        "--input", data_file,
        "--output", str(output_path / "visualization")
    ], capture_output=True, text=True)
    
    if result2.returncode == 0:
        print("     ✅ Complete")
    else:
        print(f"     ⚠️  Warning: {result2.stderr[:100]}")
    
    # 3. Counter-reward检测
    print("  3. Detecting counter-reward behaviors...")
    result3 = subprocess.run([
        "python3", "scripts/detect_counter_reward.py",
        "--input", data_file,
        "--output", str(output_path / "counter_reward")
    ], capture_output=True, text=True)
    
    if result3.returncode == 0:
        print("     ✅ Complete")
    else:
        print(f"     ⚠️  Warning: {result3.stderr[:100]}")
    
    return output_path


