def main():
    parser = argparse.ArgumentParser(description='MOSS 72h Auto-Assessment')
    parser.add_argument('--data', required=True, help='Path to actions.jsonl file')
    parser.add_argument('--output', default='docs/assessments/72h_complete', 
                       help='Output directory')
    parser.add_argument('--skip-analysis', action='store_true',
                       help='Skip running analysis tools')
    args = parser.parse_args()
    
    print("=" * 70)
    print("🤖 MOSS 72h Experiment Auto-Assessment")
    print("=" * 70)
    
    # 1. 检查实验状态
    print("\n1️⃣  Checking experiment status...")
    status = check_experiment_status()
    if status:
        print(f"   ✅ Status: {status.get('status', 'N/A')}")
    else:
        print("   ⚠️  Using default status")
        status = {"location": "Alibaba Cloud", "status": "Complete"}
    
    # 2. 运行分析
    if not args.skip_analysis:
        print("\n2️⃣  Running analysis tools...")
        output_path = run_analysis(args.data, args.output)
    else:
        output_path = Path(args.output)
        print("\n2️⃣  Skipping analysis (using existing data)")
    
    # 3. 生成报告
    print("\n3️⃣  Generating assessment report...")
    report_file = generate_assessment_report(output_path, status)
    
    # 4. 创建外部评估请求
    print("\n4️⃣  Creating external assessment request...")
    request_file = create_external_request(output_path)
    
    # 5. 总结
    print("\n" + "=" * 70)
    print("✅ Auto-assessment complete!")
    print("=" * 70)
    print(f"\n📁 Output directory: {output_path}")
    print(f"📄 Assessment report: {report_file}")
    print(f"📄 External request: {request_file}")
    print("\n📋 Next steps:")
    print("   1. Review the assessment report")
    print("   2. Fill in manual analysis sections")
    print("   3. Send external request to AI evaluators")
    print("   4. Collect and integrate feedback")
    print("=" * 70)


if __name__ == "__main__":
    main()
